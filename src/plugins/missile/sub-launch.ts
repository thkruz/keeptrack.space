/**
 * Submarine launch positioning for scripted mass raids.
 *
 * The raid sim files bake every launcher's trajectory from a fixed homeport placeholder
 * (the "Ohio Sub" shots start over North Dakota). At load time each nation's SLBM
 * launchers are gathered onto a realistic number of hulls - never more than the class's
 * real fleet - and each hull is placed at a distinct open-ocean patrol point spread
 * across that class's operating basins. Every launcher on a hull then re-flies from that
 * point to its own baked target (see {@link planSubmarineBoats}); the trajectory itself
 * is regenerated by the caller with `ballistic-trajectory.generateBallisticTrajectory`.
 *
 * This keeps the submarine COUNT realistic (a raid never implies 100 boats) while
 * spreading those boats widely instead of stacking them in one patrol box.
 */

/** Broad ocean basin a patrol box sits in; fleets patrol a subset of basins. */
export type OceanBasin = 'atlantic' | 'arctic' | 'pacific' | 'indian';

/** A rectangular patch of open ocean (deep water, clear of coasts) usable as an SSBN patrol box. */
export interface OceanRegion {
  name: string;
  ocean: OceanBasin;
  latMin: number;
  latMax: number;
  lonMin: number;
  lonMax: number;
}


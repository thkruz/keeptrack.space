/** Fallback fleet for an unrecognized submarine class. */
const DEFAULT_FLEET: SubFleet = { fleetSize: 4, capacity: 16, rangeKm: 10_000, oceans: ['atlantic', 'pacific'] };

/** The submarine class of a raid entry: the launcher name before the " (" in its `desc`. */
export const subClassOf = (desc: string | undefined): string => (desc ?? '').split(' (')[0].trim();

/** A submarine-launched raid entry awaiting a launch position (keeps its own baked target). */
export interface SubLaunchEntry {
  /** Index of this entry in the raid array (used to map the result back). */
  index: number;
  desc: string;
  /** The entry's baked target - used only to pick the nearest in-range boat. */
  targetLat: number;
  targetLon: number;
}

interface LatLon {
  lat: number;
  lon: number;
}

/** A random point uniformly inside an ocean region. */
const randomPointInRegion = (region: OceanRegion, rng: () => number): LatLon => ({
  lat: region.latMin + rng() * (region.latMax - region.latMin),
  lon: region.lonMin + rng() * (region.lonMax - region.lonMin),
});

/** Fisher-Yates shuffle into a new array, driven by the injected rng. */
const shuffled = <T>(items: readonly T[], rng: () => number): T[] => {
  const out = items.slice();

  for (let i = out.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));

    [out[i], out[j]] = [out[j], out[i]];
  }

  return out;
};

/**
 * Plan a launch position for every submarine-launched raid entry.
 *
 * Entries are grouped by boat class and placed on `min(fleetSize, count)` distinct hulls -
 * i.e. the whole fleet deploys when there are enough missiles, so the boats spread as
 * widely as the class really can, but never more hulls than exist. Each hull sits at a
 * distinct random point in one of its class's patrol basins (Chinese boats stay in the
 * Pacific, Russian boats reach the Arctic, etc.), preferring basins in range of the
 * group's targets. Every entry is then assigned to the nearest hull that can range its
 * own baked target, so a boat's missiles stay geographically sensible.
 *
 * Returns a map from entry index -> its `{ launchLat, launchLon }`. `rng` is injectable
 * for deterministic tests. Targets are NOT modified here - each entry keeps the aimpoint
 * the generator baked, so RV labels stay correct.
 */
export const planSubmarineBoats = (entries: readonly SubLaunchEntry[], rng: () => number = Math.random): Map<number, { launchLat: number; launchLon: number }> => {
  const byClass = new Map<string, SubLaunchEntry[]>();

  for (const entry of entries) {
    const cls = subClassOf(entry.desc);
    const group = byClass.get(cls);

    if (group) {
      group.push(entry);
    } else {
      byClass.set(cls, [entry]);
    }
  }

  const result = new Map<number, { launchLat: number; launchLon: number }>();

  for (const [cls, group] of byClass) {
    const fleet = SUBMARINE_FLEETS[cls] ?? DEFAULT_FLEET;
    const basinRegions = OCEAN_PATROL_REGIONS.filter((r) => fleet.oceans.includes(r.ocean));
    const regions = basinRegions.length > 0 ? basinRegions : OCEAN_PATROL_REGIONS.slice();
    // Prefer patrol boxes within missile range of at least one target the group is striking.
    const inRange = regions.filter((r) => group.some((e) => greatCircleKm(regionCenterLat(r), regionCenterLon(r), e.targetLat, e.targetLon) <= fleet.rangeKm));
    const useRegions = shuffled(inRange.length > 0 ? inRange : regions, rng);

    // Deploy the whole fleet (up to one boat per missile) so the hulls spread out.
    const boatCount = Math.max(1, Math.min(fleet.fleetSize, group.length));
    const boats = Array.from({ length: boatCount }, (_unused, i) => randomPointInRegion(useRegions[i % useRegions.length], rng));
    const load = new Array<number>(boatCount).fill(0);

    for (const entry of group) {
      // Spread each boat's fire: among the hulls that can range this target, pick the
      // least-loaded one (falling back to the single nearest hull if none is in range).
      // Load-balancing is what actually surfaces the whole fleet - a pure "nearest boat"
      // rule collapses onto one hull whenever the targets cluster.
      let chosen = -1;

      for (let b = 0; b < boatCount; b++) {
        if (greatCircleKm(boats[b].lat, boats[b].lon, entry.targetLat, entry.targetLon) > fleet.rangeKm) {
          continue;
        }
        if (chosen === -1 || load[b] < load[chosen]) {
          chosen = b;
        }
      }

      if (chosen === -1) {
        chosen = nearestBoat(boats, entry.targetLat, entry.targetLon);
      }

      load[chosen]++;
      result.set(entry.index, { launchLat: boats[chosen].lat, launchLon: boats[chosen].lon });
    }
  }

  return result;
};

/** Index of the boat closest (great-circle) to a target lat/lon. */
const nearestBoat = (boats: readonly { lat: number; lon: number }[], targetLat: number, targetLon: number): number => {
  let chosen = -1;
  let nearestDist = Infinity;

  for (let b = 0; b < boats.length; b++) {
    const d = greatCircleKm(boats[b].lat, boats[b].lon, targetLat, targetLon);

    if (d < nearestDist) {
      nearestDist = d;
      chosen = b;
    }
  }

  return chosen;
};

/** Great-circle distance (km) between two lat/lon points on a spherical Earth. */
const greatCircleKm = (lat1: number, lon1: number, lat2: number, lon2: number): number => {
  const dLat = (lat2 - lat1) * DEG2RAD;
  const dLon = (lon2 - lon1) * DEG2RAD;
  const a = Math.sin(dLat / 2) ** 2 + Math.cos(lat1 * DEG2RAD) * Math.cos(lat2 * DEG2RAD) * Math.sin(dLon / 2) ** 2;

  return 2 * EARTH_RADIUS_KM * Math.asin(Math.min(1, Math.sqrt(a)));
};

/** Center latitude of an ocean region. */
const regionCenterLat = (r: OceanRegion): number => (r.latMin + r.latMax) / 2;
/** Center longitude of an ocean region. */
const regionCenterLon = (r: OceanRegion): number => (r.lonMin + r.lonMax) / 2;

/**
 * Pick a random open-ocean launch point for a single submarine firing at the given target.
 *
 * Regions within a plausible SLBM range of the target are the candidates (falling back to
 * the single nearest region if the target is unusually remote); one is chosen at random
 * and a uniform random point inside it is returned. `rng` is injectable so tests are
 * deterministic. Used for one-off relocations; mass raids use {@link planSubmarineBoats}.
 */
export const randomOceanLaunchPoint = (targetLat: number, targetLon: number, rng: () => number = Math.random): { lat: number; lon: number } => {
  const distTo = (r: OceanRegion): number => greatCircleKm(regionCenterLat(r), regionCenterLon(r), targetLat, targetLon);
  let candidates = OCEAN_PATROL_REGIONS.filter((r) => distTo(r) <= MAX_SLBM_RANGE_KM);

  if (candidates.length === 0) {
    // Target unusually remote: fall back to the single nearest region.
    candidates = [[...OCEAN_PATROL_REGIONS].sort((a, b) => distTo(a) - distTo(b))[0]];
  }

  const region = candidates[Math.floor(rng() * candidates.length)];

  return {
    lat: region.latMin + rng() * (region.latMax - region.latMin),
    lon: region.lonMin + rng() * (region.lonMax - region.lonMin),
  };
};
